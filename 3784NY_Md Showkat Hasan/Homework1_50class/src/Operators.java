
public class Operators {


    public static void main(String[] args) {


        int score = 75;

        score = 250 + (score + 50);

        System.out.println("Score is : "+ score);


        score = 250 + (score - 50);

        System.out.println("Score is : "+ score);


        score = 250 + (score / 50);

        System.out.println("Score is : "+ score);


        score = 250 + (score * 50);

        System.out.println("Score is : "+ score);

    }
}
